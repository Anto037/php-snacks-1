<!-- Condizioni in PHP
- Crea una variabile `$eta` e assegnale un valore.  
- Se `$eta` è maggiore o uguale a 18, stampa `"Sei maggiorenne"`, altrimenti stampa `"Sei minorenne"`.   -->

<?php
    $età = 10;

    if ($età >= 18) {
        echo  "Sei maggiorenne";
    } else {
        echo  "Sei minorenne";
    }
?>